diccionario = {"a": 1, "b": 2, "c": 3, "d": 4}

# keys
# values
# items

dict_keys = tuple(diccionario.keys())
print(dict_keys)
# ('a', 'b', 'c', 'd')

dict_values = tuple(diccionario.values())
print(dict_values)
# (1, 2, 3, 4)

dict_items = tuple(diccionario.items())
print(dict_items)
# (('a', 1), ('b', 2), ('c', 3), ('d', 4))